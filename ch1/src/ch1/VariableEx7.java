package ch1;

public class VariableEx7 {
	public static void main(String[] args) {
	   int a=10, b=3;
	   //정수의 나눗셈 -몫만 출력
	   System.out.println(a/b);
	   //정수의 나눗셈 -나머지만 출력
	   System.out.println(a%b);
	   //정수의 나눗셈 -소숫점까지 출력 : 부동소숫점을 입력한다.
	   double d=10.0;
	   System.out.println(d/b);
		
		
		
	}

}
