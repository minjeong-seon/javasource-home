package ch4;

public class DoWhileEx1 {

	public static void main(String[] args) {
		//do-while문 : while문 변형으로 조건식과 블럭의 순서를 바꿔놓은 것
		//			  -->while문 뒤에 항상 ;semicolon이 와야 한다.
		//For, While은 조건이 false면 블럭을 실행하지 않음
		//do-while은 조건이 false여도 블럭이 한번은 실행됨(실행 후 검토)
		int input=0;   //변수 선언 및 초기화
		
		do {
			System.out.println("Hello");   //반복할 내용 설정
		} while (input!=0);   //언제까지 반복할지 조건 지정

		
		
		
		
		
	}

}
